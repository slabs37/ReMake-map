def convert(input_path: str, output_path: str, clip_name: str,
            min_gap: float, min_value_change: float, sample_rate: int, loop: bool) -> None:
    raw = bsor2json(input_path)

    raw = re.sub(r",\s*,", ",", raw)

    frames_raw: list[dict] = json.loads(raw)
    print(f"  Loaded {len(frames_raw)} raw keyframes")

    frames = deduplicate(frames_raw, min_gap, min_value_change=min_value_change)
    print(f"  {len(frames)} keyframes kept after deduplication (min_gap={min_gap}s)")

    if not frames:
        print("ERROR: no keyframes remain after deduplication.", file=sys.stderr)
        sys.exit(1)

    stop_time = fmt(frames[-1]["time"], 5)

    paths = ["head", "left_hand", "right_hand"]
    paths = [p for p in paths if p in frames[0]]

    # --- Rotation curves (quaternion, m_RotationCurves) ----------------------
    rot_curve_blocks = []
    for path in paths:
        def quat_xyzw(f, _path=path):
            r = f[_path]["rotation"]
            return r["x"], r["y"], r["z"], r["w"]  # Return as a tuple

        rot_curve_blocks.append(build_rotation_curve(frames, path, quat_xyzw))
    rotation_curves_str = "\n".join(rot_curve_blocks)

    # --- Position curves (m_PositionCurves) ----------------------------------
    pos_curve_blocks = []
    for path in paths:
        def pos_xyz(f, _path=path):
            p = f[_path]["position"]
            return p["x"], p["y"], p["z"]  # Return as a tuple

        pos_curve_blocks.append(build_vector_curve(frames, path, pos_xyz))
    position_curves_str = "\n".join(pos_curve_blocks)

    # --- Editor curves (one per channel) ------------------------------------
    editor_curve_blocks = []
    pos_axes = [
        ("m_LocalPosition.x", lambda f, p: f[p]["position"]["x"]),
        ("m_LocalPosition.y", lambda f, p: f[p]["position"]["y"]),
        ("m_LocalPosition.z", lambda f, p: f[p]["position"]["z"]),
    ]
    rot_axes = [
        ("m_LocalRotation.x", lambda f, p: f[p]["rotation"]["x"]),
        ("m_LocalRotation.y", lambda f, p: f[p]["rotation"]["y"]),
        ("m_LocalRotation.z", lambda f, p: f[p]["rotation"]["z"]),
        ("m_LocalRotation.w", lambda f, p: f[p]["rotation"]["w"]),
    ]
    for path in paths:
        for attr, fn in pos_axes:
            editor_curve_blocks.append(
                build_editor_curve(
                    frames, path, attr,
                    lambda f, _fn=fn, _path=path: _fn(f, _path),
                    is_rotation=False,
                )
            )
    for path in paths:
        for attr, fn in rot_axes:
            editor_curve_blocks.append(
                build_editor_curve(
                    frames, path, attr,
                    lambda f, _fn=fn, _path=path: _fn(f, _path),
                    is_rotation=True,
                )
            )
    editor_curves_str = "\n".join(editor_curve_blocks)

    bindings_str = build_bindings(paths)

    yaml_text = YAML_TEMPLATE.format(
        clip_name=clip_name,
        rotation_curves=rotation_curves_str,
        position_curves=position_curves_str,
        bindings=bindings_str,
        stop_time=stop_time,
        loop_time=1 if loop else 0,
        sample_rate=sample_rate,
        editor_curves=editor_curves_str,
    )

    out = Path(output_path)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(yaml_text, encoding="utf-8")
    print(f"Written to {output_path}")
    print(f"  Clip name : {clip_name}")
    print(f"  Stop time : {stop_time}s")
    print(f"  Keyframes : {len(frames)}")
    print(f"  Paths     : {paths}")
    

def bsor2json(input_path: str):
    with open(input_path, 'rb') as f:
        print(f)
        m = make_bsor(f)
        data = json.loads(str(m))
        frames = data.get("frames", [])
        return(str(frames).replace("'", '"'))



def build_bindings(paths: list[str]) -> str:
    # attribute 1 = m_LocalPosition  (customType 0)
    # attribute 2 = m_LocalRotation quaternion (customType 0)
    lines = []
    for path in paths:
        h = unity_path_hash(path)
        lines.append(BINDING_TEMPLATE.format(
            path_hash=h, attribute=2, custom_type=0))
    for path in paths:
        h = unity_path_hash(path)
        lines.append(BINDING_TEMPLATE.format(
            path_hash=h, attribute=1, custom_type=0))
    return "\n".join(lines)



def build_editor_curve(frames: list[dict], path: str, attribute: str,
                       value_fn, is_rotation: bool = False) -> str:
    values = [value_fn(f) for f in frames]
    times  = [f["time"] for f in frames]

    if is_rotation:
        # Set all slopes to 0
        slopes = [0.0 for _ in range(len(frames))]
        kf_lines = "\n".join(
            KEYFRAME_ROTATION_TEMPLATE.format(
                time=fmt(f["time"], 5),
                value=fmt(values[i]),
                in_slope=fmt(slopes[i]) if i > 0 else "0",
                out_slope=fmt(slopes[i]) if i < len(frames) - 1 else "0",
            )
            for i, f in enumerate(frames)
        )
    else:
        kf_lines = "\n".join(
            KEYFRAME_SCALAR_TEMPLATE.format(time=fmt(f["time"], 5), value=fmt(values[i]))
            for i, f in enumerate(frames)
        )
    return CURVE_BLOCK_TEMPLATE.format(keyframes=kf_lines, attribute=attribute, path=path)



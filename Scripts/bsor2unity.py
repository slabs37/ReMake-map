# Unity path-hash helper
def unity_path_hash(path: str) -> int:
    """Return the unsigned 32-bit CRC32 Unity uses for a transform path."""
    crc = zlib.crc32(path.encode("utf-8")) & 0xFFFFFFFF
    return crc


# helpers
def fmt(v: float, digits: int = 4) -> str:
    rounded = round(v, digits)
    if rounded == 0.0:
        return "0"
    # Format without trailing zeros
    text = f"{rounded:.{digits}f}".rstrip("0").rstrip(".")
    return text


def deduplicate(frames: list[dict], min_gap: float, min_value_change: float = 0.0) -> list[dict]:
    if not frames:
        return []

    def get_values(f: dict) -> list[float]:
        vals = []
        for path in ("head", "left_hand", "right_hand"):
            if path not in f:
                continue
            p = f[path]["position"]
            r = f[path]["rotation"]
            vals += [p["x"], p["y"], p["z"], r["x"], r["y"], r["z"], r["w"]]
        return vals

    kept = [frames[0]]
    last_vals = get_values(frames[0])

    for frame in frames[1:]:
        curr_vals = get_values(frame)
        time_ok = (frame["time"] - kept[-1]["time"]) >= min_gap
        value_ok = True
        for i in range(len(curr_vals)):
            if (abs(curr_vals[i] - last_vals[i]) < min_value_change):
                value_ok = False
                
        if time_ok and value_ok if value_ok != 0 else time_ok:
            kept.append(frame)
            last_vals = curr_vals

    return kept


